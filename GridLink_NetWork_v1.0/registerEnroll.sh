#!/bin/bash

function createProducer() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/producer.gridlink.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/producer.gridlink.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-producer --tls.certfiles "${PWD}/organizations/fabric-ca/producer/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-producer.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-producer.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-producer.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-producer.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/producer.gridlink.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy producer's CA cert to producer's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/producer.gridlink.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/producer/ca-cert.pem" "${PWD}/organizations/peerOrganizations/producer.gridlink.com/msp/tlscacerts/ca.crt"

  # Copy producer's CA cert to producer's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/producer.gridlink.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/producer/ca-cert.pem" "${PWD}/organizations/peerOrganizations/producer.gridlink.com/tlsca/tlsca.producer.gridlink.com-cert.pem"

  # Copy producer's CA cert to producer's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/producer.gridlink.com/ca"
  cp "${PWD}/organizations/fabric-ca/producer/ca-cert.pem" "${PWD}/organizations/peerOrganizations/producer.gridlink.com/ca/ca.producer.gridlink.com-cert.pem"

  echo "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-producer --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/producer/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering user"
  set -x
  fabric-ca-client register --caname ca-producer --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/producer/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-producer --id.name produceradmin --id.secret produceradminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/producer/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-producer -M "${PWD}/organizations/peerOrganizations/producer.gridlink.com/peers/peer0.producer.gridlink.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/producer/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/producer.gridlink.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/producer.gridlink.com/peers/peer0.producer.gridlink.com/msp/config.yaml"

  echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-producer -M "${PWD}/organizations/peerOrganizations/producer.gridlink.com/peers/peer0.producer.gridlink.com/tls" --enrollment.profile tls --csr.hosts peer0.producer.gridlink.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/producer/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/producer.gridlink.com/peers/peer0.producer.gridlink.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/producer.gridlink.com/peers/peer0.producer.gridlink.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/producer.gridlink.com/peers/peer0.producer.gridlink.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/producer.gridlink.com/peers/peer0.producer.gridlink.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/producer.gridlink.com/peers/peer0.producer.gridlink.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/producer.gridlink.com/peers/peer0.producer.gridlink.com/tls/server.key"

  echo "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-producer -M "${PWD}/organizations/peerOrganizations/producer.gridlink.com/users/User1@producer.gridlink.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/producer/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/producer.gridlink.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/producer.gridlink.com/users/User1@producer.gridlink.com/msp/config.yaml"

  echo "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://produceradmin:produceradminpw@localhost:7054 --caname ca-producer -M "${PWD}/organizations/peerOrganizations/producer.gridlink.com/users/Admin@producer.gridlink.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/producer/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/producer.gridlink.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/producer.gridlink.com/users/Admin@producer.gridlink.com/msp/config.yaml"
}

